package christmas.domain;

public class GiftMenuInfo {

    private String name;
    private int counts;

    public void setName(String name) {
        this.name = name;
    }

    public void setCounts(int counts) {
        this.counts = counts;
    }

    public String getName() {
        return name;
    }

    public int getCounts() {
        return counts;
    }
}
